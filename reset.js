class Reset
{
    constructor(str, pos)
    {
        this.str = str;
        this.pos = pos;
        this.button = createButton('Reset');
    }

    drawButton()
    {
        this.button.size(this.str.x, this.str.y);
        this.button.position(this.pos.x, this.pos.y);
        this.button.mousePressed
        (
            cirkelAntal = antalAlle
        );
    }
}